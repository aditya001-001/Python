# Variables and Data Types

# Integer
age = 21

# Float
height = 5.8

# String
name = "Aditya"

# Boolean
is_student = True

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Type Checking
print(type(age))
print(type(height))
print(type(name))
print(type(is_student))

# Type Conversion
num = "50"
converted_num = int(num)
print(converted_num + 10)
